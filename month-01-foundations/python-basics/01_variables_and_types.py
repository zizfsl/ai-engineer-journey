# 01_variables_and_types.py

#printing a string
print("Hello, World!")

# Integer
age = 25
print(type(age))

# Float
salary = 50000.75
print(type(salary))

# String
name = "Faisal"
print(type(name))

# Boolean
is_engineer = True
print(type(is_engineer))

# Arithmetic
x = 10
y = 3
print(x + y)
print(x / y)

# Type casting
num_str = "100"
num_int = int(num_str)
print(num_int + 50)

# f-string
print(f"My name is {name} and I am {age} years old.")